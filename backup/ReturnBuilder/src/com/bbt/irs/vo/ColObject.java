package com.bbt.irs.vo;

/**
 *
 * @author tkola
 */
public class ColObject {

    private String colRef;
    private String colHeader;
    /**
     * @return the colRef
     */
    public String getColRef() {
        return colRef;
    }

    /**
     * @param colRef the colRef to set
     */
    public void setColRef(String colRef) {
        this.colRef = colRef;
    }

    /**
     * @return the colHeader
     */
    public String getColHeader() {
        return colHeader;
    }

    /**
     * @param colHeader the colHeader to set
     */
    public void setColHeader(String colHeader) {
        this.colHeader = colHeader;
    }
    
}
